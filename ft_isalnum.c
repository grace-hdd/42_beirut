int	ft_isalnum(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if (((str[c] < 'A') || (str[c] > 'Z'))
			&& ((str[c] < 'a') || (str[c] > 'z'))
			&& ((str[c] < 0) || (str[c] > 9)))
			return (0);
		c++;
	}
	return (1);
}
